package fishShop;

public class Rod {
private String brand;
private String price;
public Rod() {
	super();
}
public Rod(String brand, String price) {
	super();
	this.brand = brand;
	this.price = price;
}
public String getBrand() {
	return brand;
}
public void setBrand(String brand) {
	this.brand = brand;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}

}
